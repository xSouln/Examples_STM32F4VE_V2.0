static void GetStatus(xRxRequestManagerT* manager)
{
	FlowDirectorT* FlowDirector = manager->Device;
	
	xRxPutInResponseBuffer(manager->RxLine, &FlowDirector->Status, sizeof(FlowDirector->Status));
}
