static void GetStatus(xRxRequestManagerT* manager)
{
	BrewGroupT* BrewGroup = manager->Device;
	
	xRxPutInResponseBuffer(manager->RxLine, &BrewGroup->Status, sizeof(BrewGroup->Status));
}
